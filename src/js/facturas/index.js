const guardarFactura = async (e) => {
    e.preventDefault();
    btnGuardar.disabled = true;
    const url = '/farmacia/controllers/facturas/index.php'
    const formData = new FormData(formulario)
    formData.append('tipo', 1)
    formData.delete('factura_id')
    const config = {
        method: 'POST',
        body: formData
    }

    try {
        const respuesta = await fetch(url, config);
        const data = await respuesta.json();
        const { mensaje, codigo, detalle } = data
        Swal.mixin({
            toast: true,
            position: "top-end",
            showConfirmButton: false,
            timer: 3000,
            timerProgressBar: true,
            icon: "success",
            title: mensaje,
            didOpen: (toast) => {
                toast.onmouseenter = Swal.stopTimer;
                toast.onmouseleave = Swal.resumeTimer;
            }
        }).fire();
        // alert(mensaje)

        if (codigo == 1 && respuesta.status == 200) {
            formulario.reset();
            getFacturas(alerta ='no');
            
        } else {
            console.log(detalle);
        }

    } catch (error) {
        console.log(error);
    }
    btnGuardar.disabled = false;
}

const llenardatos = (factura) => {
    console.log(factura);
    formulario.factura_id.value = factura.factura_id
    formulario.factura_cliente.value = factura.cliente_id
    formulario.factura_producto.value = factura.producto_id
    formulario.factura_cantidad.value = factura.factura_cantidad
    formulario.factura_fecha.value = factura.factura_fecha
    btnGuardar.parentElement.style.display = 'none'
    btnBuscar.parentElement.style.display = 'none'
    btnLimpiar.parentElement.style.display = 'none'
    btnModificar.parentElement.style.display = ''
    btnCancelar.parentElement.style.display = ''
}

